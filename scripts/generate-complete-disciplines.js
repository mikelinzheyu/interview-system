function statsData(data) {
  let mgCount = 0, mCount = 0, sCount = 0

  data.forEach(disc => {
    disc.majorGroups.forEach(mg => {
      mgCount++
      mg.majors.forEach(m => {
        mCount++
        sCount += m.specializations.length
      })
    })
  })

  return { mgCount, mCount, sCount }
}

