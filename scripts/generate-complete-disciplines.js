function generateCompleteData() {
  const result = []

  for (const [discCode, discData] of Object.entries(COMPLETE_DISCIPLINES)) {
    const majorGroups = []

    for (const [mgCode, mgData] of Object.entries(discData.majorGroups || {})) {
      const majors = []

      for (const [majorCode, majorData] of Object.entries(mgData.majors || {})) {
        majors.push(createMajor(majorCode, majorData.name, majorData.desc))
      }

      majorGroups.push(createMajorGroup(mgCode, mgData.name, majors))
    }

    result.push({
      id: discCode,
      code: discCode,
      name: discData.name,
      icon: discData.icon,
      description: discData.description,
      color: discData.color,
      majorGroups: majorGroups
    })
  }

  return result
}

